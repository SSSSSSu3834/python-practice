# \n: 줄바꿈
print("백문이 불여일견 \n백견이 불여일타")

# 저는 "김현수" 입니다 출력하기
print("저는 \"김현수\" 입니다")

# \\: 문장내에서는 하나의 \
print("gustn\\컴공\\메롱")

# \r : 커서를 맨 앞으로 이동
# \r이 들어간 곳에서부터 맨 앞으로 커서를 옮겨서 뒤에 있는 내용을 덮어쓰기 한다.
print("Red Apple\rPine")  #PineApple


# \b : 백스페이스 (한 글자 삭제)
print("redd\bApple")

#\t : tab
print("red\tApple") #red     Apple
